// Traditional function expression
const multiply = function(a,b) {
    return a * b; 
}; 

// Lambda function : provide more compact syntax compared to traditional function expressions
const multiply2 = (a, b) => a * b; 
console.log(multiply2(2, 3)); // Output: 6 

